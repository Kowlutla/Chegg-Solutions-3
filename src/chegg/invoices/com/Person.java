package chegg.invoices.com;

public class Person {

	// Variables
	private String mName;

	/**
	 * Constructor
	 * 
	 * @param mName
	 */
	public Person(String mName) {
		super();
		this.mName = mName;
	}

	/**
	 * Getter method for mName
	 * 
	 * @return the mName
	 */
	public String getmName() {
		return mName;
	}

	/**
	 * Setter method for mName
	 * 
	 * @param mName the mName to set
	 */
	public void setmName(String mName) {
		this.mName = mName;
	}

}
